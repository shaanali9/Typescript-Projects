const Sum=(numberOne:number,numberTwo:number):number =>{
    return numberOne + numberTwo
};
const Multiply=(numberOne:number,numberTwo:number):number =>{
    return numberOne * numberTwo
};
const Division=(numberOne:number,numberTwo:number):number =>{
    return numberOne / numberTwo
};
const Subtract=(numberOne:number,numberTwo:number):number =>{
    return numberOne - numberTwo
};

